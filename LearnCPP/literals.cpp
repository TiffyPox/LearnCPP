#include <iostream>

int main()
{
    // a value such as 20, is known as a literal because its value self-evident

    int a = 20; // decimal notation (signed)
    int b = 024; // octal notation (signed or unsigned)
    int c = 0x14; // hexadecimal notation (s or u)

    // floating-point literals include either a decimal point or using scientific notation (E or e)

    int d = 2.1234;
    int e = 2.1234E0;
    int f = 2.;
    int g = 2e2;
    int h = .001;

    return 0;
}